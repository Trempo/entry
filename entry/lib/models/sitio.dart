class Sitio {
  final String nombre;
  final String ciudad;
  final String pais;
  final String direccion;
  final String telefono;
  final int aforo;
  final String tipo;
  final String descripcion;

  Sitio(this.nombre, this.ciudad, this.pais, this.direccion, this.telefono,
      this.aforo, this.tipo, this.descripcion);
}
